#include "libft.h"

void	ft_bzero(void *ptr, size_t len)
{
	size_t	loop;
	char	*pr;

	loop=0;
	pr = ptr;
	if (!ptr)
		return ;
	while(loop++ < len)
	{
		*pr = 0;
		ptr++;
	}
}
